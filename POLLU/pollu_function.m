function dydt = pollu_function(y)
%% Chemical Parameters definition
% rate constants
k1 = 3.5e-1;
k2 = 2.66e1;
k3 = 1.23e4;
k4 = 8.6e-4;
k5 = 8.2e-4;
k6 = 1.5e4;
k7 = 1.3e-4;
k8 = 2.4e4;
k9 = 1.65e4;
k10 = 9e3;
k11 = 2.2e-2;
k12 = 1.2e4;
k13 = 1.88;
k14 =  1.63e4;
k15 = 4.8e6;
k16 = 3.5e-4;
k17 = 1.75e-2;
k18 = 1e8;
k19 = 4.44e11;
k20 = 1.24e3;
k21 = 2.1;
k22 = 5.78;
k23 = 4.74e-2;
k24 = 1.78e3;
k25 = 3.12;

% reaction velocities
r1 = k1*y(:, 1);
r2 = k2*y(:, 2).*y(:, 4);
r3 = k3*y(:, 5).*y(:, 2);
r4 = k4*y(:, 7);
r5 = k5*y(:, 7);
r6 = k6*y(:, 7).*y(:, 6);
r7 = k7*y(:, 9);
r8 = k8*y(:, 9).*y(:, 6);
r9 = k9*y(:, 11).*y(:, 2);
r10 = k10*y(:, 11).*y(:, 1);
r11 = k11*y(:, 13);
r12 = k12*y(:, 10).*y(:, 2);
r13 = k13*y(:, 14);
r14 = k14*y(:, 1).*y(:, 6);
r15 = k15*y(:, 3);
r16 = k16*y(:, 4);
r17 = k17*y(:, 4);
r18 = k18*y(:, 16);
r19 = k19*y(:, 16);
r20 = k20*y(:, 17).*y(:, 6);
r21 = k21*y(:, 19);
r22 = k22*y(:, 19);
r23 = k23*y(:, 1).*y(:, 4);
r24 = k24*y(:, 19).*y(:, 1);
r25 = k25*y(:, 20);

dydt = [- r1 - r10 - r14 - r23 - r24 + r2 + r3 + r9 + r11 + r12 + r22 + r25, ...
        - r2 - r3 - r9 - r12 + r1 + r21, ...
        - r15 + r1 + r17 + r19 + r22, ...
        - r2 - r16 - r17 - r23 + r15, ... 
        - r3 + 2*r4 + r6 + r7 + r13 + r20, ...
        - r6 - r8 - r14 - r20 + r3 + 2*r18, ...
        - r4 - r5 - r6 + r13, ...
        r4 + r5 + r6 + r7, ...
        - r7 - r8, ...
        - r12 + r7 + r9, ... 
        - r9 - r10 + r8 + r11, ... 
        r9, ...
        - r11 + r10, ...
        - r13 + r12, ...
        r14, ...
        - r18 - r19 + r16, ...
        - r20, ...
        r20, ...
        - r21 - r22 - r24 + r23 + r25, ...
        - r25 + r24];

end

